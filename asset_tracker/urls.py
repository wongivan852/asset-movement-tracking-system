from django.contrib import admin
from django.urls import path, include
from django.conf import settings
from django.conf.urls.static import static
from rest_framework_simplejwt.views import (
    TokenObtainPairView,
    TokenRefreshView,
    TokenVerifyView,
)
from drf_spectacular.views import (
    SpectacularAPIView,
    SpectacularRedocView,
    SpectacularSwaggerView
)

urlpatterns = [
    path('admin/', admin.site.urls),

    # Phase 1: SSO Authentication (django-allauth)
    # Provides OAuth/OIDC login, signup, and social account management
    path('accounts/', include('allauth.urls')),

    # Local accounts management (profile, user management)
    # These override/extend allauth functionality
    path('accounts/', include('accounts.urls')),

    # Phase 1: REST API with JWT Authentication
    path('api/token/', TokenObtainPairView.as_view(), name='token_obtain_pair'),
    path('api/token/refresh/', TokenRefreshView.as_view(), name='token_refresh'),
    path('api/token/verify/', TokenVerifyView.as_view(), name='token_verify'),

    # Phase 2: API Documentation
    path('api/schema/', SpectacularAPIView.as_view(), name='schema'),
    path('api/docs/', SpectacularSwaggerView.as_view(url_name='schema'), name='swagger-ui'),
    path('api/redoc/', SpectacularRedocView.as_view(url_name='schema'), name='redoc'),

    # Phase 2: API v1 Endpoints
    # path('api/v1/', include('api.v1.urls')),  # Uncomment when API v1 URLs are created

    # Application URLs
    path('', include('dashboard.urls')),
    path('assets/', include('assets.urls')),
    path('locations/', include('locations.urls')),
    path('movements/', include('movements.urls')),
]

# Serve media files in development
if settings.DEBUG:
    urlpatterns += static(settings.MEDIA_URL, document_root=settings.MEDIA_ROOT)
    urlpatterns += static(settings.STATIC_URL, document_root=settings.STATIC_ROOT)
